# დღეს გადამეორებულიც განსაკუთრებით
num = int(input("100-დან 999-მდე რიცხვი: "))
if 100 <= num <= 999:
    hundreds = num // 100
    tens = (num % 100) // 10
    units = num % 10
    print(f"ასეულები: {hundreds}, ათეულები: {tens}, ერთეულები: {units}")